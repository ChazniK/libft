#include "libft.h"

void	ft_lstdelone(t_list **alst, void (*del)(void*, size_t))
{
	t_list	*list;

	list = *alst;
	*alst = (*alst)->next;
	del(list->content, list->content_size);
	free(list);
	list = NULL;
}
