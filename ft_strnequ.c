#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (!(s1 && s2))
		return (0);
	while ((s1[i] && s2[j]) && (s1[i] == s2[j]) && (n > 0))
	{
		i++;
		j++;
		n--;
	}
	if ((s1[i] == '\0' && s2[j] == '\0') || (n == 0))
		return (1);
	return (0);
}
